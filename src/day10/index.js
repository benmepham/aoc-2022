import run from "aocrunner";

const parseInput = (rawInput) =>
  rawInput.split("\n").map((line) => line.split(" "));

const part1 = (rawInput) => {
  const input = parseInput(rawInput);
  let startVal = 1;
  let valX = [];
  for (const line of input) {
    if (line[0] === "noop") {
      valX.push(startVal);
    } else if (line[0] === "addx") {
      valX.push(startVal);
      startVal += parseInt(line[1]);
      valX.push(startVal);
    }
  }
  return (
    valX[18] * 20 +
    valX[58] * 60 +
    valX[98] * 100 +
    valX[138] * 140 +
    valX[178] * 180 +
    valX[218] * 220
  );
};

